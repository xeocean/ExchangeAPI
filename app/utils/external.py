def request_currency_list(search_code: str):
    list_url = URL + f"/{search_code}"
    currency_list = requests.get(list_url)
    if currency_list.status_code == 200:
        response: dict = currency_list.json()["conversion_rates"]
        return response
    raise HTTPException(status_code=400, detail="Not found")


def request_currency_translation(currency_dict: dict):
    code_value_1 = currency_dict.get("value_1")
    code_value_2 = currency_dict.get("value_2")
    quantity = currency_dict.get("quantity")
    if code_value_1 in currency and code_value_2 in currency:
        dict_for_code = request_currency_list(code_value_1)
        rate = dict_for_code[code_value_2]
        return rate * quantity
    raise HTTPException(status_code=400, detail="Invalid code")


